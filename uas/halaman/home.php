		<h1>Pengertian PHP dan Fungsi PHP</h1>

		<h2>Pengertian PHP</h2>
		<p>
			PHP  (PHP: Hypertext Preprocessor) adalah sebuah bahasa pemrograman server side scripting yang bersifat open source. Sebagai sebuah scripting language, PHP menjalankan instruksi pemrograman saat proses runtime. Hasil dari instruksi tentu akan berbeda tergantung data yang diproses. <br><br>

			PHP merupakan bahasa pemrograman server-side, maka script dari PHP nantinya akan diproses di server. Jenis server yang sering digunakan bersama dengan PHP antara lain Apache, Nginx, dan LiteSpeed. <br><br>

			Selain itu, PHP juga merupakan bahasa pemrograman yang bersifat open source. Pengguna bebas memodifikasi dan mengembangkan sesuai dengan kebutuhan mereka. Tentunya jika memiliki kemampuan coding yang baik.
		</p>

		<h2>Mengapa Menggunakan PHP?</h2>
		<p>
			Saat ini, tak kurang dari 78% website di seluruh dunia menggunakan bahasa pemrograman yang diciptakan Rasmus Lerdorf di tahun 1995 ini. Bahkan platform besar seperti Facebook juga menggunakannya. <br><br>

			Lalu, apa yang membuat PHP begitu populer? Mengapa menggunakan PHP yang sudah berumur lebih dari dua dekade ini? Berikut beberapa alasannya: <br>
			<ul>
				<li><b>Cenderung mudah dipelajari</b> — dibanding beberapa bahasa pemrograman populer lain, PHP lebih mudah dipelajari.</li>
				<li><b>Materi belajar yang melimpah</b> — umur PHP yang “cukup tua” menyebabkan banyak sekali dokumentasi, panduan, dan komunitas aktif bertebaran di jagat maya. Jadi, tak perlu takut jika Anda mengalami kesulitan.</li>
				<li><b>PHP bersifat open-source</b> — siapapun bisa menggunakan PHP tanpa mengeluarkan biaya sepeserpun.</li>
				<li><b>Kecepatan tinggi</b> — PHP terbukti bisa meningkatkan kecepatan loading dibanding bahasa lain. Misalnya, lebih cepat tiga kali daripada Phyton pada beberapa kasus.</li>
				<li><b>Banyaknya pilihan database</b> — PHP bisa digunakan di hampir semua jenis database. Mulai dari MySQL, hingga non-relational database seperti Redis.</li>
				<li><b>Kompatibilitas yang baik dengan HTML</b> — script PHP tidak mengganggu HTML sama sekali. Justru mereka berdua saling melengkapi.</li>
				<li><b>Fleksibilitas tinggi</b> — PHP bisa dikombinasikan dengan banyak sekali bahasa pemrograman lain. Sehingga bisa Anda gunakan sesuai kebutuhan.</li>
				<li><b>Multi-platform</b> — PHP bisa Anda gunakan di macam-macam operating system. Mulai dari Windows, Linux, hingga MacOS.</li>
				<li><b>Selalu diperbarui</b> — sejak pertama kali muncul tahun 1995, sekarang PHP sudah berada pada versi 7.4.</li>
				<li><b>Mendukung layanan cloud</b> — siapa sangka, walaupun umur PHP hampir dua dekade, tapi ia bisa mendukung layanan cloud dengan skalabilitas yang baik.</li>
			</ul>
		</p>

		<h2>Fungsi PHP</h2>
		<p>
			Untuk membuat halaman web, sebenarnya PHP bukanlah bahasa pemrograman yang wajib digunakan. Kita bisa saja membuat website hanya menggunakan HTML saja. Web yang dihasilkan dengan HTML (dan CSS) ini dikenal dengan website statis, dimana konten dan halaman web bersifat tetap. <br><br>

			Sebagai perbandingan, website dinamis yang bisa dibuat menggunakan PHP adalah situs web yang bisa menyesuaikan tampilan konten tergantung situasi. Website dinamis juga bisa menyimpan data ke dalam database, membuat halaman yang berubah-ubah sesuai input dari user, memproses form, dll. Untuk pembuatan web, kode PHP biasanya di sisipkan kedalam dokumen HTML. Karena fitur inilah PHP disebut juga sebagai Scripting Language atau bahasa pemrograman script.
